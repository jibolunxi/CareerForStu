package domain;

public class CompanyJobMin {
	private int id;
	private String name;
	private int jobhits;
	private int snum;
	private int status;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getJobhits() {
		return jobhits;
	}
	public void setJobhits(int jobhits) {
		this.jobhits = jobhits;
	}
	public int getSnum() {
		return snum;
	}
	public void setSnum(int snum) {
		this.snum = snum;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	
	
	
}
